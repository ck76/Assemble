import torch
import torchvision
import torchvision.transforms as transforms

# https://www.jianshu.com/p/74dd44f1e654
# TODO 是对数据进行归一化的操作 image = (image - mean) / std
# https://blog.csdn.net/bublebee/article/details/88993467
# 比如原来的tensor是三个维度的，值在0到1之间，那么经过以下变换之后就到了-1到1区间。 
#
# transforms.Normalize((.5,.5,.5),(.5,.5,.5))
# 原本以为第一个参数应该是（0，0，0）才是归一化到均值为0。但是通过transforms的源码发现：
#
# ``input[channel] = (input[channel] - mean[channel]) / std[channel]``
# 也就是说(（0,1）-0.5）/0.5=(-1,1)。
#
# 即参数为变换过程，而非最终结果。
transform = transforms.Compose(
    [transforms.ToTensor(),
     transforms.Normalize(mean=(0.5, 0.5, 0.5), std=(0.5, 0.5, 0.5))])

trainset = torchvision.datasets.CIFAR10(root='./data', train=True,
                                        download=True, transform=transform)
trainloader = torch.utils.data.DataLoader(trainset, batch_size=4,
                                          shuffle=True, num_workers=2)

testset = torchvision.datasets.CIFAR10(root='./data', train=False,
                                       download=True, transform=transform)
testloader = torch.utils.data.DataLoader(testset, batch_size=4,
                                         shuffle=False, num_workers=2)

